    v_username VARCHAR2(20);
BEGIN
    FOR i IN 1..50 LOOP
        v_username := 'user' || LPAD(i, 3, '0');
		FOR dt IN (SELECT TO_DATE('2024-04-11', 'YYYY-MM-DD') + LEVEL - 1 AS log_date FROM DUAL CONNECT BY LEVEL <= 7) LOOP
            INSERT INTO S24_S003_T9_CustomerLogs (Username, LogID, LogDate, WaterIntake, StepsWalked, IsDietFollowed, IsWorkoutFollowed)
            SELECT
                v_username,
                SEQ_S24_S003_T9_CustomerLogs.NEXTVAL,
                dt.log_date,
				ROUND(DBMS_RANDOM.VALUE(1, 4)),
                ROUND(DBMS_RANDOM.VALUE(5000, 15000)),
                CASE WHEN DBMS_RANDOM.VALUE() < 0.5 THEN 'Yes' ELSE 'No' END,
                CASE WHEN DBMS_RANDOM.VALUE() < 0.5 THEN 'Yes' ELSE 'No' END
            FROM dual
			WHERE NOT EXISTS (
                SELECT 1
                FROM S24_S003_T9_CustomerLogs cl
                WHERE cl.Username = v_username
                AND cl.LogDate = dt.log_date
            );
        END LOOP;
    END LOOP;
END;
